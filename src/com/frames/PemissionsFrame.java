    private void fillPermissionTable() {
        Object columnNames[] = {"permission_id","permission"};
        tableModel = new DefaultTableModel(columnNames, 0);
        List<Permissions> list = pDao.getAllPermissions();
        for (Permissions p : list) {
           
            Object rowData[] = {p.getPermissionId(),p.getPermission()};
            tableModel.addRow(rowData);
            PermissionTable.setModel(tableModel);
        }
    }

