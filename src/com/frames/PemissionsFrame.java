    private void ADDbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDbtnActionPerformed
        // TODO add your handling code here:
        String permission=permissionField.getText();
        Permissions p=new Permissions();
        p.setPermission(permission);
        int row=pDao.addPermission(p);
        
       if(row>0)
       {
           JOptionPane.showMessageDialog(this,"Added Sucessfully" );
       }
       else
       {
           JOptionPane.showMessageDialog(this,"Error" );
       }
       fillPermissionTable();
        
    }//GEN-LAST:event_ADDbtnActionPerformed

    private void PermissionTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PermissionTableMouseClicked
        // TODO add your handling code here:
        Integer id= (Integer) PermissionTable.getValueAt(PermissionTable.getSelectedRow(), 0);
        Permissions p=pDao.getPermissionById(id);
        permissionField.setText(p.getPermission());
    }//GEN-LAST:event_PermissionTableMouseClicked

    private void clearBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearBtnActionPerformed
        // TODO add your handling code here:
        permissionField.setText("");
    }//GEN-LAST:event_clearBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
       Integer id= (Integer) PermissionTable.getValueAt(PermissionTable.getSelectedRow(), 0);
       pDao.deletePermission(id);
       tableModel.removeRow(PermissionTable.getSelectedRow());
       PermissionTable.clearSelection();
       fillPermissionTable();
        
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
       Integer id= (Integer) PermissionTable.getValueAt(PermissionTable.getSelectedRow(), 0);
       Permissions p=pDao.getPermissionById(id);
       String permission=permissionField.getText();
      
       p.setPermission(permission);
       pDao.updatePermission(p);
       fillPermissionTable();
    }//GEN-LAST:event_updateBtnActionPerformed

