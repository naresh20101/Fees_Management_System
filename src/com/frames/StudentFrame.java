     private void fillCountryTable() {
        Object columnNames[] = {"std_id","name","department","PhoneNo","Address","date_of_addmition","Batch_id","fees_id"};
        tableModel = new DefaultTableModel(columnNames, 0);
        List<Student> list = studentDAO.getStudents();
        for (Student s : list) {
           Batch b=batchDao.getBatchById(s.getBatch().getBatch_id());
           Fees f=feesDao.getFeesById(s.getFees().getFees_id());
            Object rowData[] = {s.getStd_id(),s.getName(),s.getDepartment(),s.getPhone_No(),s.getAddress(),s.getDate_of_addmition(),b.getBatch_name(),f.getFees()};
            tableModel.addRow(rowData);
            StudentTable.setModel(tableModel);
        }
    }

    public void fillBatchCombo() {
        List<Batch> list = batchDao.getAllBatches();
        batchCombo.addItem("--Select Batch--");
        for (Batch db : list) {
            batchCombo.addItem(db.getBatch_name());
        }
    }

    public void fillFeesCombo() {
        List<Fees> list = feesDao.getAllFees();
        feesCombo.addItem("--Select Fees--");
        for (Fees db : list) {
            feesCombo.addItem(db.getFees());
        }
    }
    public void filterContactList(String query) {
        TableRowSorter<DefaultTableModel> tableRowSorter = new TableRowSorter<>(tableModel);
        StudentTable.setRowSorter(tableRowSorter);
        tableRowSorter.setRowFilter(RowFilter.regexFilter(query));
    }

