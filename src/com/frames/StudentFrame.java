    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here

        String std_name = stdNameField.getText();

        String dept = deptField.getText();
        String Address = addressField.getText();
        Integer PhoneNo = Integer.parseInt(phoneField.getText());
        System.out.println("dateee " + dateFields.getDate());
       // SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");
       // formater.format(dateFields.getDate());
       // Date date = (Date) dateFields.getDate();

        String dist = batchCombo.getSelectedItem().toString();
        Integer batch_Id = batchDao.getBatchByName(dist);
        Integer amount =  (Integer) feesCombo.getSelectedItem();
        Integer fees_id = feesDao.getFeesIdByFees(amount);
        Batch b = new Batch();
        b.setBatch_id(batch_Id);
        Fees f = new Fees();
        f.setFees_id(fees_id);

        Student s = new Student();

        s.setName(std_name);

        s.setAddress(Address);
        s.setPhone_No(PhoneNo);
        s.setDate_of_addmition(dateFields.getDate());
        s.setBatch(b);
        s.setFees(f);
        s.setDepartment(dept);
        // System.out.println("dateee "+);

        int row = studentDAO.addStudent(s);
        if (row > 0) {
            JOptionPane.showMessageDialog(this, "Added Sucessfully");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        fillCountryTable();


    }//GEN-LAST:event_addBtnActionPerformed

