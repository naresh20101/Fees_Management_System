     private void fillUserTable() {
        Object columnNames[] = {"user_id","user_name","passward","Email","PhoneNo","Address","user_role"};
        tableModel = new DefaultTableModel(columnNames, 0);
        List<User> list = uDao.getUsers();
        for (User u: list) {
           Roll r=rollDao.getRollById(u.getRoll().getRollId());
         
            Object rowData[] = {u.getUserId(),u.getUserName(),u.getPassword(),u.getEmail(),u.getPhoneNo(),u.getAddress(),r.getRoleName()};
            tableModel.addRow(rowData);
            UserTable.setModel(tableModel);
        }
    }
    private void fillRollCombo() {
        List<Roll> list = rollDao.getAllRoll();
        RoleCombo.addItem("--Select Role--");
        for (Roll r : list) {
            RoleCombo.addItem(r.getRoleName());
        }
    }
