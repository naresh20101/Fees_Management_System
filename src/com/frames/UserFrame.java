    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        String username= usernameField.getText();
        String passward=passwordField.getText();
        String email=EmailField.getText();
        String address=addressField.getText();
        String phoneNo=phoneField.getText();
        String role=RoleCombo.getSelectedItem().toString();
        Integer rolleId=rollDao.getRollByName(role);
        Roll r=new Roll();
        r.setRollId(rolleId);
        User u=new User();
        u.setAddress(address);
        u.setEmail(email);
        u.setPassword(passward);
        u.setRoll(r);
        u.setUserName(username);
        u.setPhoneNo(phoneNo);
        int row=uDao.addUser(u);
         if (row > 0) {
            JOptionPane.showMessageDialog(this, "Added Sucessfully");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        fillUserTable();  
    }//GEN-LAST:event_AddBtnActionPerformed

    private void UserTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UserTableMouseClicked
        // TODO add your handling code here:
        Integer user_id=(Integer) UserTable.getValueAt(UserTable.getSelectedRow(), 0);
        User u=uDao.getUserById(user_id);
        usernameField.setText(u.getUserName());
        passwordField.setText(u.getPassword());
        EmailField.setText(u.getEmail());
        addressField.setText(u.getAddress());
       phoneField.setText(u.getPhoneNo());
       Roll r=rollDao.getRollById(u.getRoll().getRollId());
       RoleCombo.setSelectedItem(r.getRoleName());
       
        
       
       
    }//GEN-LAST:event_UserTableMouseClicked

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        Integer user_id=(Integer) UserTable.getValueAt(UserTable.getSelectedRow(), 0);
        uDao.deleteStudent(user_id);
        System.out.println("Deleted");
        tableModel.removeRow(UserTable.getSelectedRow());
        UserTable.clearSelection();
        fillUserTable();
    }//GEN-LAST:event_deleteBtnActionPerformed

